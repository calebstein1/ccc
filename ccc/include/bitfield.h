#ifndef CCC_BITFIELD_H
#define CCC_BITFIELD_H

#define S0 0x01 /* Set bit 0 */
#define S1 0x02 /* Set bit 1 */
#define S2 0x04 /* Set bit 2 */
#define S3 0x08 /* Set bit 3 */
#define S4 0x10 /* Set bit 4 */
#define S5 0x20 /* Set bit 5 */
#define S6 0x40 /* Set bit 6 */
#define S7 0x80 /* Set bit 7 */

#define U0 0xfe /* Clear bit 0 */
#define U1 0xfd /* Clear bit 1 */
#define U2 0xfb /* Clear bit 2 */
#define U3 0xf7 /* Clear bit 3 */
#define U4 0xef /* Clear bit 4 */
#define U5 0xdf /* Clear bit 5 */
#define U6 0xbf /* Clear bit 6 */
#define U7 0x7f /* Clear bit 7 */

#endif /* CCC_BITFIELD_H */
